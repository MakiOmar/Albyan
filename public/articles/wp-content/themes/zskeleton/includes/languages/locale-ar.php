/**
 * Apply Arabic translations when site locale is Arabic (fallback if .mo missing).
 *
 * @param string $translation Translated text.
 * @param string $text        Original text.
 * @param string $domain      Text domain.
 * @return string
 */
function zskeleton_gettext_ar_fallback($translation, $text, $domain) {
    if ('zskeleton' !== $domain) {
        return $translation;
    }
    $locale = function_exists('determine_locale') ? determine_locale() : get_locale();
    if (stripos($locale, 'ar') !== 0) {
        return $translation;
    }
    $map = zskeleton_get_arabic_gettext_map();
    // Use compiled .mo when it provides a real string; otherwise use map (covers missing/empty .mo and empty msgstr in PO).
    $trim_tr = is_string($translation) ? trim($translation) : '';
    $has_mo  = (is_string($translation) && $translation !== $text && '' !== $trim_tr);
    if ($has_mo) {
        return $translation;
    }
    if (isset($map[$text])) {
        return $map[$text];
    }
    return '' === $trim_tr && is_string($translation) ? $text : $translation;
}
add_filter('gettext', 'zskeleton_gettext_ar_fallback', 10, 3);
